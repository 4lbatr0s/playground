import logging
import threading
import time



'''
#INFO: Execute the main function multiple times in a row.
You'll likely see the finishing order of threads changes frequently.
This is because operating system decides which threads will execute first after its started
To predict, exactly in what order the operating system will execute the threads is impossible to know.
So, be aware of this feature when you use threading in your designs.
'''

def thread_function(name):
    logging.info("Thread %s: starting", name)
    time.sleep(2)
    logging.info("Thread %s: finishing", name)

if __name__ == "__main__":
    format = "%(asctime)s: %(message)s"
    logging.basicConfig(format=format, level=logging.INFO,
                        datefmt="%H:%M:%S")

    threads = list()
    for index in range(3):
        logging.info("Main    : create and start thread %d.", index)
        x = threading.Thread(target=thread_function, args=(index,))
        threads.append(x)
        x.start()

    for index, thread in enumerate(threads):
        logging.info("Main    : before joining thread %d.", index)
        thread.join()
        logging.info("Main    : thread %d done", index)